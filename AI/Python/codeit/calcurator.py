def addition(num1,num2):
    return num1 + num2

def subtraction(num1,num2):
    return num1 - num2

def multiplication(num1,num2):
    return num1 * num2

def division(num1,num2):
    return num1 / num2

print(addition(5,8))
print(subtraction(5,2))
print(multiplication(7,2))
print(division(8,2))

#floor division (버림 나눗셈)
print(8//3)
print(8.0//3)

#round (반올림)
print(round(3.1415926535))
print(round(3.1415926535,4)) #소수점 자리 지정